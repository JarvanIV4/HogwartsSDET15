# 计算器（加减乘除）
class Calculator:
    def add(self, a, b):
        return a + b

    def add1(self, a: int, b: int) -> int:
        return a + b

    def sub(self, a, b):
        return a - b

    def mul(self, a, b):
        return a * b

    def div(self, a, b):
        return a / b
